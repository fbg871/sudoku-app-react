export const convertCodetoNumber = (code: string) => {
	if (code === 'Digit1') {
		return 1
	} else if (code === 'Digit2') {
		return 2
	} else if (code === 'Digit3') {
		return 3
	} else if (code === 'Digit4') {
		return 4
	} else if (code === 'Digit5') {
		return 5
	} else if (code === 'Digit6') {
		return 6
	} else if (code === 'Digit7') {
		return 7
	} else if (code === 'Digit8') {
		return 8
	} else if (code === 'Digit9') {
		return 9
	}
}
